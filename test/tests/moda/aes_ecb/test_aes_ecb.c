#include "unity.h"

#include "moda.h"
#include "aes.h"
#include "aes_ecb.h"

void test_MODA_AES_ECB_Encrypt(void)
{
    static const uint8_t key[] = {0x10,0xa5,0x88,0x69,0xd7,0x4b,0xe5,0xa3,0x74,0xcf,0x86,0x7c,0xfb,0x47,0x38,0x59};
    static const uint8_t pt[] = {0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00};
    static const uint8_t ct[] = {0x6d,0x25,0x1e,0x69,0x44,0xb0,0x51,0xe0,0x4e,0xaa,0x6f,0xb4,0xdb,0xf7,0x84,0x65,0x6d,0x25,0x1e,0x69,0x44,0xb0,0x51,0xe0,0x4e,0xaa,0x6f,0xb4,0xdb,0xf7,0x84,0x65};

    struct aes_ctxt aes;
    uint8_t out[AES_BLOCK_SIZE*2];

    MODA_AES_Init(&aes, AES_KEY_128, key);
    MODA_AES_ECB_Encrypt(&aes, out, pt, sizeof(out));

    TEST_ASSERT_EQUAL_MEMORY(ct, out, sizeof(ct));
}

